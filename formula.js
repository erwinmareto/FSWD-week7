// Menghitung keliling persegi 
exports.squarePerimeter = (sides) => { return 4 * sides};

// Menghitung luas persegi
exports.squareArea = (sides) => { return sides ** 2};

// Menghitung keliling persegi panjang
exports.rectanglePerimeter = (length, width) => { return 2 * (length + width)};

// Menghitung luas persegi panjang
exports.rectangleArea = (length, width) => { return length * width };


